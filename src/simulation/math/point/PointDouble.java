package simulation.math.point;

/**
 * Point of double type
 * 
 * @author SOARES Lucas
 */
public class PointDouble extends Point<Double>
{
	/**
	 * Construct the double point
	 * 
	 * @param x
	 * 		The x to set
	 * @param y
	 * 		The y to set
	 */
	public PointDouble( Double x,
		Double y )
	{
		super( x,
			y );
	}
}
